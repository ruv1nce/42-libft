#include "libft.h"

void	ft_print_hex(unsigned int nb)
{
	char	*sym;

	sym = "0123456789abcdef";
	if (nb / 16 > 0)
		ft_print_hex(nb / 16);
	ft_putchar(sym[nb % 16]);
}
