#include "libft.h"

char	*ft_strncat(char *s1, const char *s2, size_t n)
{
	unsigned int	i;
	size_t			j;

	i = 0;
	j = 0;
	while (s1[i])
	{
		i++;
	}
	while (*s2 && j < n)
	{
		s1[i] = *s2;
		i++;
		j++;
		s2++;
	}
	s1[i] = '\0';
	return (s1);
}
